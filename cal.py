num1 = float(input('Digite o primeiro número: '))
num2 = float(input('Digite o segundo número: '))

#adição
print('{} + {} = '.format(num1, num2))
print (num1 + num2)

#subtração
print('{} - {} = '.format(num1, num2))
print(num1 + num2)

#multiplicação
print('{} * {} = '.format(num1, num2))
print(num1 * num2)

#divisão
print('{} / {} = '.format(num1, num2))
print(num1 / num2)