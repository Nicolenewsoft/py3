#Atribuir valores a própria variável

a = 3
a = a + 7

print(a)

#acrescentando 6 ao valor de a que era 10
a += 6
print (a)

#retirando 3 do valor de a
a -= 3
print (a)

#multiplicando o valor por 2
a *= 2
print (a)

#Dividindo por 4 o valor de a
a /= 4
print (a)

#Pegando o módulo da divisão
a %= 4
print (a)

#Esponencial: Pegar o valor de a e elevando a 8
a **= 8
print (a)

#Forçando um valor inteiro na divisão
a //= 127
print (a)