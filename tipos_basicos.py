#Tipo Boleano
print(True)
print(False)

#Float e Inteiro
print(1.2 + 1)

#String com aspas duplas ou não
print('Olá mundo')
print("Olá mundo")

#Concatenação: Junção de tipos
print('você é ' + 3 * 'muito ' + 'legal')

#Lista
print([1, 2, 3])

#Dicionário: Chave: valor
print({'nome': 'Nicole', 'idade' : '22'})

#Nulo, sem valor
print(None)